def read_file(path: Path, max_lines: int = 500) -> str:
    """Read a file, truncating to max_lines if needed."""
    try:
        with open(path, "r", encoding="utf-8", errors="replace") as f:
            lines = f.readlines()
        if len(lines) > max_lines:
            lines = lines[:max_lines] + [f"\n... (truncated, {len(lines)} lines total)\n"]
        return "".join(lines)
    except OSError:
        return ""


def collect_source_files(lib_dir: Path) -> dict[str, str]:
    """Collect all .m files from a library directory (non-recursive, skip private/)."""
    files = {}
    if not lib_dir.is_dir():
        return files
    for mfile in sorted(lib_dir.glob("*.m")):
        rel = mfile.relative_to(PROJECT_ROOT)
        files[str(rel)] = read_file(mfile)
    return files


def collect_examples() -> dict[str, str]:
    """Collect example files (first 100 lines each for context)."""
    files = {}
    if not EXAMPLES_DIR.is_dir():
        return files
    for mfile in sorted(EXAMPLES_DIR.glob("*.m")):
        rel = mfile.relative_to(PROJECT_ROOT)
        files[str(rel)] = read_file(mfile, max_lines=100)
    return files


def collect_all_sources() -> dict[str, str]:
    """Collect all relevant source files."""
    sources = {}
    for lib_name in ["FastPlot", "Dashboard", "SensorThreshold", "EventDetection", "WebBridge"]:
        sources.update(collect_source_files(LIBS_DIR / lib_name))

    # Include README
    readme = PROJECT_ROOT / "README.md"
    if readme.exists():
        sources["README.md"] = read_file(readme)

    # Include setup.m
    setup = PROJECT_ROOT / "setup.m"
    if setup.exists():
        sources["setup.m"] = read_file(setup)

    # Include design docs
    for md in sorted(DOCS_DIR.rglob("*.md")):
        rel = md.relative_to(PROJECT_ROOT)
        sources[str(rel)] = read_file(md, max_lines=300)

    return sources


def build_source_context(sources: dict[str, str], max_chars: int = 180000) -> str:
    """Build a source context string, respecting a character budget."""
    parts = []
    total = 0
    for path, content in sorted(sources.items()):
        entry = f"=== {path} ===\n{content}\n"
        if total + len(entry) > max_chars:
            parts.append(f"\n... (budget reached, {len(sources) - len(parts)} files omitted)\n")
            break
        parts.append(entry)
        total += len(entry)
    return "".join(parts)


