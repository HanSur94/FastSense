def call_claude(client: anthropic.Anthropic, system_prompt: str, user_prompt: str) -> str:
    """Call Claude API and return the response text."""
    for attempt in range(3):
        try:
            response = client.messages.create(
                model=MODEL,
                max_tokens=MAX_TOKENS,
                system=system_prompt,
                messages=[{"role": "user", "content": user_prompt}],
            )
            return response.content[0].text
        except anthropic.RateLimitError:
            wait = 2 ** (attempt + 1)
            print(f"    Rate limited, waiting {wait}s...")
            time.sleep(wait)
        except anthropic.APIError as e:
            if attempt < 2:
                wait = 2 ** (attempt + 1)
                print(f"    API error: {e}, retrying in {wait}s...")
                time.sleep(wait)
            else:
                raise
    raise RuntimeError("Failed after 3 retries")


def generate_page(client: anthropic.Anthropic, page: dict, source_context: str,
                   example_context: str) -> str:
    """Generate a single wiki page using Claude."""
    system_prompt = """\
You are a technical documentation writer for FastPlot, an ultra-fast time series \
plotting library for MATLAB and GNU Octave. You write clear, accurate, well-structured \
GitHub Wiki pages in Markdown.

Rules:
- Use accurate information from the provided source code — do not invent APIs or features
- Use GitHub-flavored Markdown with proper headings, code blocks, and tables
- MATLAB code blocks should use ```matlab fencing
- Keep explanations concise but thorough
- Do not include the page title as an H1 — it will be added automatically
- Do not include any auto-generated notice — it will be added automatically
- Reference actual class names, method signatures, and property names from the source
- Link to other wiki pages where relevant using [[Page Name]] syntax"""

    user_prompt = f"""{page['prompt']}

## Source Code Reference

{source_context}

## Example Scripts Reference

{example_context}"""

    return call_claude(client, system_prompt, user_prompt)


