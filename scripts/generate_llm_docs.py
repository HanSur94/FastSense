def main():
    api_key = os.environ.get("ANTHROPIC_API_KEY")
    if not api_key:
        print("ERROR: ANTHROPIC_API_KEY environment variable is required", file=sys.stderr)
        sys.exit(1)

    print(f"FastPlot LLM Documentation Generator")
    print(f"Project root: {PROJECT_ROOT}")
    print(f"Wiki dir:     {WIKI_DIR}")
    print(f"Model:        {MODEL}")
    print()

    # Collect source files
    print("Collecting source files...")
    sources = collect_all_sources()
    print(f"  Found {len(sources)} source files")

    print("Collecting examples...")
    examples = collect_examples()
    print(f"  Found {len(examples)} example files")

    source_context = build_source_context(sources)
    example_context = build_source_context(examples, max_chars=60000)
    print(f"  Source context: {len(source_context):,} chars")
    print(f"  Example context: {len(example_context):,} chars")
    print()

    # Initialize client
    client = anthropic.Anthropic(api_key=api_key)

    # Ensure wiki directory exists
    WIKI_DIR.mkdir(parents=True, exist_ok=True)

    # Generate each page
    generated = []
    for page in PAGES:
        filename = page["filename"]
        print(f"Generating {filename}...")

        content = generate_page(client, page, source_context, example_context)

        # Write output
        outpath = WIKI_DIR / filename
        full_content = AUTO_NOTICE + f"# {page['title']}\n\n" + content + "\n"
        with open(outpath, "w", encoding="utf-8") as f:
            f.write(full_content)

        print(f"  -> Wrote {outpath.relative_to(PROJECT_ROOT)} ({len(content):,} chars)")
        generated.append(filename)

    print()
    print(f"Done. Generated {len(generated)} wiki pages:")
    for f in generated:
        print(f"  - {f}")


if __name__ == "__main__":
    main()
